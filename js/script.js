const trip = document.getElementById("trip");
trip.addEventListener("mouseenter", function(event) {
    this.classList.add("bigger")
    // console.log(event)
});
trip.addEventListener("mouseleave", function(event) {
    this.classList.remove("bigger")
    // console.log(event)
})
